public class Rectangulo {

    public int calcularArea(int base, int altura){
        return base * altura;
    }
    public double calcularArea(double base, double altura){
        return base * altura;
    }


    public int calcutarPerimetro(int base, int altura){
        return (2 * base) + (2 * altura);
    }
    public double calcutarPerimetro(double base, double altura){
        return (2 * base) + (2 * altura);
    }
}
